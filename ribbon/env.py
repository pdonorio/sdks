""" Module to load .env variables and JSON constants """
# ---------------------------------------------------------------------------

# ---------------------------------------------------------------------------
# Imports
# ---------------------------------------------------------------------------
import os
import json
from dotenv import load_dotenv

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------
ADDRESSES_JSON = "./constants/addresses.json"
CHAINS = ["kovan", "mainnet"]

# ---------------------------------------------------------------------------
# Load Variables
# ---------------------------------------------------------------------------
load_dotenv()

RPCS = {
  chain: {
    "url": os.environ.get(f'{chain.upper()}_URL'), 
    "token": os.environ.get(f'{chain.upper()}_TOKEN')
  } for chain in CHAINS
}

# ---------------------------------------------------------------------------
# Load JSON Constants
# ---------------------------------------------------------------------------
with open(ADDRESSES_JSON) as f:
  ADDRESSES = json.load(f)